def create_bar_chart(data, title, xlabel, ylabel, rotation=45):
    """
    Cette fonction cree un graphique en barres Matplotlib.
    """

    fig, ax = plt.subplots(figsize=(10, 5))

    ax.bar(data.index, data.values)

    ax.set_title(title)
    ax.set_xlabel(xlabel)
    ax.set_ylabel(ylabel)

    plt.xticks(rotation=rotation, ha="right")
    plt.tight_layout()

    return fig


def filter_watch_history(df, selected_genre, selected_min_rating):
    """
    Cette fonction applique les filtres selectionnes par l'admin.
    """

    filtered_df = df.copy()

    if selected_genre != "All":
        filtered_df = filtered_df[filtered_df["genre"] == selected_genre]

    filtered_df = filtered_df[filtered_df["rating"] >= selected_min_rating]

    return filtered_df


def show_kpi_cards(df, movies_df, users_df):
    """
    Cette fonction affiche les cartes KPI principales.
    """

    total_movies = movies_df.shape[0]
    total_users = users_df.shape[0]
    total_views = df.shape[0]

    if df.empty:
        average_rating = 0
        dominant_genre = "N/A"
        most_active_user = "N/A"
    else:
        average_rating = round(df["rating"].mean(), 2)
        dominant_genre = df["genre"].value_counts().idxmax()
        most_active_user = df["user_name"].value_counts().idxmax()

    col1, col2, col3, col4, col5, col6 = st.columns(6)

    col1.metric("Films", total_movies)
    col2.metric("Utilisateurs", total_users)
    col3.metric("Visionnages", total_views)
    col4.metric("Note moyenne", average_rating)
    col5.metric("Genre dominant", dominant_genre)
    col6.metric("Top utilisateur", most_active_user)


def plotly_bar_chart(df, x, y, title):
    """
    Cette fonction cree un graphique en barres interactif avec Plotly.
    """

    fig = px.bar(
        df,
        x=x,
        y=y,
        title=title,
        template="plotly_dark"
    )

    fig.update_layout(
        paper_bgcolor="rgba(0,0,0,0)",
        plot_bgcolor="rgba(17,24,39,0.8)",
        font=dict(color="white"),
        title_font=dict(size=22, color="white")
    )

    return fig


