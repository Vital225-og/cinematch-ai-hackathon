@st.cache_data
def load_data():
    """
    Cette fonction genere les donnees brutes, nettoie les donnees,
    cree la matrice utilisateur-film et initialise le moteur de recommandation.
    """

    movies_df = generate_movies()

    users_df = generate_users(number_of_users=30)

    raw_watch_history_df = generate_watch_history(
        users_df,
        movies_df,
        make_dirty=True
    )

    clean_watch_history_df = clean_watch_history(raw_watch_history_df)

    user_movie_matrix = create_user_movie_matrix(clean_watch_history_df)

    recommender = MovieRecommender(
        movies_df=movies_df,
        watch_history_df=clean_watch_history_df,
        user_movie_matrix=user_movie_matrix
    )

    return (
        movies_df,
        users_df,
        raw_watch_history_df,
        clean_watch_history_df,
        user_movie_matrix,
        recommender
    )


